def update_blocks_data_js(file_path):
    """blocks-data.js 파일 업데이트"""
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # 각 블록의 objectives 배열 업데이트
    for block_id in range(1, 18):
        objectives = OBJECTIVES_3AREAS[block_id]
        
        # objectives 배열을 JavaScript 형식으로 변환
        objectives_js = '[\n'
        for obj in objectives:
            objectives_js += f'            "{obj}",\n'
        objectives_js = objectives_js.rstrip(',\n') + '\n        ]'
        
        # 패턴: id: N, ... objectives: [...], ... 찾기
        pattern = rf'(id: {block_id},.*?objectives: )\[(.*?)\]'
        
        def replace_func(match):
            prefix = match.group(1)
            return f'{prefix}{objectives_js}'
        
        content = re.sub(pattern, replace_func, content, flags=re.DOTALL, count=1)
    
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(content)
    
    return True

def main():
    """메인 실행 함수"""
    base_dir = Path(__file__).parent
    
    # 업데이트할 파일들
    files_to_update = [
        base_dir / "docs" / "blocks-data.js",
        base_dir / "report" / "blocks-data.js"
    ]
    
    updated_count = 0
    
    for file_path in files_to_update:
        if file_path.exists():
            if update_blocks_data_js(file_path):
                print(f"✅ {file_path.relative_to(base_dir)}")
                updated_count += 1
        else:
            print(f"⚠️  {file_path.relative_to(base_dir)} 파일 없음")
    
    print(f"\n✨ 총 {updated_count}개 blocks-data.js 파일 업데이트 완료")

if __name__ == "__main__":
    main()
